    public static String trimToNull(String s) {
        if(s == null)
            return null;
        s = s.trim();

        if(s.length() == 0)
            return null;

        return s;
    }

    public static boolean isEquals(String s1, String s2) {
        if(s1 == null)
            return s2 == null;

        if(s1.length() != s2.length())
            return false;

        return s1.equals(s2);
    }
